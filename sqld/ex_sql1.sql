-- job 전체의 개수와 합계
SELECT COUNT(*) 개수, SUM(salary) 급여총계
FROM job;

-- job_id 별 개수와 급여 총계
SELECT job_id, COUNT(*) 개수, SUM(salary) 급여총계
FROM job 
GROUP BY job_id;

-- job_id 별 급여 총계와 소계
SELECT job_id, SUM(salary) salary
FROM job 
GROUP BY ROLLUP(job_id)
ORDER BY job_id;

-- job_id별 급여 최대값 구하기
SELECT job_id, MAX(salary) 급여최대
FROM job 
GROUP BY job_id;

-- job_id별 급여 최소값 구하기
SELECT job_id, MIN(salary) 급여최소
FROM job 
GROUP BY job_id;

-- 최대값, 최소값 더하기(합집합) - UNION
SELECT job_id, MAX(salary) 급여최대
FROM job 
GROUP BY job_id
UNION
SELECT job_id, MIN(salary) 급여최소
FROM job 
GROUP BY job_id;

